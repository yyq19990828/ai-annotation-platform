#!/usr/bin/env node
// 扫描 apps/api/app/api/v1/ 下的路由模块，生成 docs-site/api/guides/_routes.generated.md
// 索引页：模块名 → 该模块所有 @router.<method> 路径。
//
// 目的：让阅读 API 指南的人能一眼看到全量路由，反查到具体模块文件。

import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(here, "../..");
const ROUTES_DIR = resolve(REPO_ROOT, "apps/api/app/api/v1");
const OUT = resolve(here, "../api/guides/_routes.generated.md");

const ROUTE_RE = /@router\.(get|post|put|patch|delete)\s*\(\s*["']([^"']+)["']/gi;
const SKIP = new Set(["__init__.py", "router.py", "_test_seed.py"]);

const modules = readdirSync(ROUTES_DIR)
  .filter((f) => f.endsWith(".py") && !SKIP.has(f))
  .sort();

let md = `<!-- generated by docs-site/scripts/generate-api-index.mjs — do not edit -->\n`;
md += `# 路由索引（自动生成）\n\n`;
md += `本页由 \`docs-site/scripts/generate-api-index.mjs\` 在 \`pnpm docs:dev/build\` 时刷新。\n\n`;
md += `按模块分组列出 \`apps/api/app/api/v1/\` 下所有 \`@router.*\` 装饰的路由。\n\n`;

let totalRoutes = 0;
for (const f of modules) {
  const src = readFileSync(resolve(ROUTES_DIR, f), "utf8");
  const routes = [];
  let m;
  ROUTE_RE.lastIndex = 0;
  while ((m = ROUTE_RE.exec(src)) !== null) {
    routes.push({ method: m[1].toUpperCase(), path: m[2] });
  }
  if (!routes.length) continue;
  md += `## \`${f}\`\n\n`;
  md += `| Method | Path |\n|---|---|\n`;
  for (const r of routes) {
    md += `| ${r.method} | \`${r.path}\` |\n`;
  }
  md += `\n`;
  totalRoutes += routes.length;
}

md += `---\n\n_共 ${modules.length} 模块 / ${totalRoutes} 路由_\n`;

writeFileSync(OUT, md);
console.log(`[generate-api-index] wrote ${totalRoutes} routes from ${modules.length} modules → ${OUT}`);
